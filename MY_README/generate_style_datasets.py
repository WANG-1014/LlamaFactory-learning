def build_dataset(inst_fn, out_fn):
    data = []
    for category, rows in tasks:
        for idx, (prompt, input_text, plain) in enumerate(rows):
            # Each category has 10 rows: 7 explicit style prompts and 3 weak style prompts.
            explicit = idx < 7
            data.append(item(inst_fn(category, prompt, explicit), out_fn(category, plain), input_text))
            inst_fn.counter += 1
            if hasattr(out_fn, "counter"):
                out_fn.counter += 1
    return data


def main():
    for filename, (inst_fn, out_fn) in styles.items():
        inst_fn.counter = 0
        if hasattr(out_fn, "counter"):
            out_fn.counter = 0
        data = build_dataset(inst_fn, out_fn)
        if len(data) != 100:
            raise RuntimeError(f"{filename}: expected 100 rows, got {len(data)}")
        path = ROOT / "data" / filename
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"wrote {path} ({len(data)} rows)")


if __name__ == "__main__":
    main()
